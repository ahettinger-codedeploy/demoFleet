// Constants
execute_featured = 1;								// Number of times to execute the generation script
execute_highlighted_featured = 1;					// Number of times to execute the generation script
execute_highlighted_highlighted = 3;				// Number of times to execute the generation script
Array_Featured_Items = new Array();					// Array of featured items currently showing
Array_Highlighted_Featured_Items = new Array();		// Array of highlighted featured items currently showing
Array_Highlighted_Highlighted_Items = new Array();	// Array of highlighted highlighted items currently showing

// Picks a featured artist and adds it to the Array_Items_X array depending on the type passed
function pickFeaturedArtist ()
{
	rand_featured = Math.floor(Math.random()*FeaturedArtist_Names.length);
	// Check for duplicates
	temp = Array_Featured_Items.join(",") + ",";
	if (temp.indexOf(rand_featured+",") == -1)
	{
		Array_Featured_Items.push(rand_featured);
	}
	else
	{
		pickFeaturedArtist();
	}
}

// Picks a highlighted artist an adds it to the Array_Items_X array depending on the type passed
function pickHighlightedArtist (passed_type)
{
	if (passed_type == "highlighted")
	{
		rand_highlighted = Math.floor(Math.random()*HighlightedArtist_Highlighted_Names.length);
		// Check for duplicates
		temp = Array_Highlighted_Highlighted_Items.join(",") + ",";
		if (temp.indexOf(rand_highlighted+",") == -1)
		{
			Array_Highlighted_Highlighted_Items.push(rand_highlighted);
		}
		else
		{
			pickHighlightedArtist("highlighted");
		}
	}
	if (passed_type == "featured")
	{
		rand_highlighted = Math.floor(Math.random()*HighlightedArtist_Featured_Names.length);
		// Check for duplicates within self
		temp = Array_Highlighted_Featured_Items.join(",") + ",";
		if (temp.indexOf(rand_highlighted+",") == -1)
		{
			// Check for duplicated within featured (flash movies)
			temp = Array_Featured_Items.join(",") + ",";
			if (temp.indexOf(rand_highlighted+",") == -1)
			{
				Array_Highlighted_Featured_Items.push(rand_highlighted);
			}
			else
			{
				pickHighlightedArtist("featured");
			}
		}
		else
		{
			pickHighlightedArtist("featured");
		}
	}
}

// Generate Featured artists
function generateFeatured ()
{
	// Pick them
	for (i=0; i<execute_featured; i++)
	{
		pickFeaturedArtist();
	}
	// Display them
	for (i=0; i<Array_Featured_Items.length; i++)
	{
		document.write("<object classid=\"clsid:D27CDB6E-AE6D-11cf-96B8-444553540000\" codebase=\"http://download.macromedia.com/pub/shockwave/cabs/flash/swflash.cab#version=7,0,19,0\" width=\"500\" height=\"200\">");
		document.write("<param name=\"movie\" value=\"" + FeaturedArtist_Images[Array_Featured_Items[i]] + "?artist_link=" + FeaturedArtist_Links[Array_Featured_Items[i]] + "\" />");
		document.write("<param name=\"quality\" value=\"high\" />");
		document.write("<embed src=\"" + FeaturedArtist_Images[Array_Featured_Items[i]] + "?artist_link=" + FeaturedArtist_Links[Array_Featured_Items[i]] + "\" quality=\"high\" pluginspage=\"http://www.macromedia.com/go/getflashplayer\" type=\"application/x-shockwave-flash\" width=\"500\" height=\"200\"></embed>");
		document.write("</object>");
	}
}

