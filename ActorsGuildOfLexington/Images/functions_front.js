
function setPointer(theRow, thePointerColor)
{
    if (thePointerColor == '' || typeof(theRow.style) == 'undefined') {
        return false;
    }
    if (typeof(document.getElementsByTagName) != 'undefined') {
        var theCells = theRow.getElementsByTagName('td');
    }
    else if (typeof(theRow.cells) != 'undefined') {
        var theCells = theRow.cells;
    }
    else {
        return false;
    }

    var rowCellsCnt  = theCells.length;
    for (var c = 0; c < rowCellsCnt; c++) {
        theCells[c].style.backgroundColor = thePointerColor;
    }

    return true;
}

function setClass(theRow, thePointerClass)
{
    if (typeof(theRow.style) == 'undefined') {
        return false;
    }else{
		theRow.className=thePointerClass;
		return true;
	}
}

function mark(face,field_color,text_color){

	if (document.documentElement){//if browser is IE5+ or NS6+
		face.style.backgroundColor=field_color;
		face.style.color=text_color;
	}

}

function menuPos(yPos) {
    var menu = document.getElementById('menu');
    menu.style.backgroundPosition= '0px '+yPos+'px';
} 

